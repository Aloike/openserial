CMenuPlugins::CMenuPlugins(QWidget *argParent)
    :   QMenu( tr("Plugins"), argParent )
{
    connect( this, SIGNAL(aboutToShow()),
             this, SLOT(on_aboutToShow()) );
}

/* ########################################################################## */
/* ########################################################################## */

void    CMenuPlugins::on_aboutToShow(void)
{
    this->clear();
}

